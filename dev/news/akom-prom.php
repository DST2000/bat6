<p>Тяговые аккумуляторы от «АКОМ» могут поставляться отдельными ячейками с номинальным напряжением 2В, а также  для удовлетворения требований заказчика  могут комплектоваться в батареи (помещаются в специальный стальной короб, соединяются перемычками, могут комплектоваться различными аксессуарами). </p>

<p>Промышленные батареи от  «АКОМ» могут быть применены на всех видах погрузчиков, штабелеров: Jungheinrich,  Still, Linde, Xilin, Noblift, Heli, Lema, JAC, Maximal, Dalian, Zowell, Liftland, Toyota, Komatsu, Nissan,Tailift, Otto Kurtbach, Tisel, Liugongl и многих других, а также уборочных машин и другой техники, имеющей электрический привод и предъявляющей требования глубокого разряда к аккумуляторам.</p>

<p>Опыт и инфраструктура лидера рынка стартерных аккумуляторов РФ ГК АКОМ* в сочетании  с комплектующими и технологиями от мировых лидеров в области промышленных аккумуляторов позволяют гарантировать высокую работоспособность промышленных аккумуляторов АКОМ и учитывать повышенные требования пользователей и сервисных компаний складской техники.</p>
