<h2>ПРОМЫШЛЕННЫЕ АККУМУЛЯТОРЫ АКОМ</h2>
<p>Завод АКОМ-Индастриал, входящий в ГК «АКОМ» начал выпуск промышленных аккумуляторов АКОМ в г.Тольятти с использованием импортных комплектующих от компании Eternity Technologies.</p>
<ul><span>Промышленные аккумуляторы АКОМ предназначены для  складской техники:</span>
	<li>электропогрузчиков;</li>
	<li>штабелеров;</li>
	<li>ричтраков.</li>
</ul>

