<div class="news-style">
<p>В последние годы владельцы крупных транспортных компании и автопарков обращают все больше внимания на общую стоимость владения транспортом. Экономия на качестве автозапчастей может привести к большим потерям.</p>
<p>Понимая потребности своих клиентов, серьезные производители комплектующих для грузовых автомобилей постоянно работают над улучшением  своих продуктов.</p>
<p>Так, производитель автомобильных аккумуляторов EXIDE провел работу по улучшению конструкции грузовых аккумуляторов серии Start Prо. Модельный ряд был расширен новыми типами с увеличенными пусковыми и ёмкостными характеристиками.</p>
