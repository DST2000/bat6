<p>Аккумуляторы Exide StartPRO изготавливаются по специальной технологии HD (Heavy Duty), которая включает:</p>	
<ul>
	<li>использование пластин  Ca\Ca</li>
	<li>изготовление положительных пластин по технологии штамповки Punch</li>
	<li>изготовление отрицательные пластины по технологии тянутого металла Exmet</li>
	<li>технологию Heavy Duty, в частности, использование стеклофлиса и усиленного сепаратора для улучшения параметра циклирования,  применение термокомпаунда для лучшей фиксации блоков пластин</li>
	<li>доступ к электролиту и возможность контроля его уровня традиционным способом</li>
</ul>
<p>Благодаря технологии HD technology аккумуляторы Exide также имеют более жесткую и прочную структуру пластин; они меньше подвергаются коррозии;  и как следствие имеют увеличенный срок службы и превосходные электрические характеристики и производительность.</p>
